const Section = ({children, title}): Node => {
  const isDarkMode = useColorScheme() === 'dark';
  return (
    <View style={styles.sectionContainer}>
      <Text
        style={[
          styles.sectionTitle,
          {
            color: isDarkMode ? Colors.white : Colors.black,
          },
        ]}>
        {title}
      </Text>
      <Text
        style={[
          styles.sectionDescription,
          {
            color: isDarkMode ? Colors.light : Colors.dark,
          },
        ]}>
        {children}
      </Text>
    </View>
  );
};

const App: () => Node = () => {
  const webviewRef = React.useRef(null);

  useEffect(() => {
    checkSupport();
  }, []);

  const checkSupport = () => {
    webviewRef.current.injectJavaScript(`
      if (typeof SharedArrayBuffer === 'undefined') {
        window.ReactNativeWebView.postMessage('SharedArrayBuffer is not supported')
      } else {
        window.ReactNativeWebView.postMessage('SharedArrayBuffer is supported')
      }
    `);
  };

  return (
    <WebView
      ref={webviewRef}
      source={{uri: '<h1>Test RN</h1>'}}
      javaScriptEnabled
      onMessage={event => {
        console.log(event.nativeEvent.data);
        Alert.alert('', JSON.stringify(event.nativeEvent.data));
      }}
    />
  );
};

const styles = StyleSheet.create({
  sectionContainer: {
    marginTop: 32,
    paddingHorizontal: 24,
  },
  sectionTitle: {
    fontSize: 24,
    fontWeight: '600',
  },
  sectionDescription: {
    marginTop: 8,
    fontSize: 18,
    fontWeight: '400',
  },
  highlight: {
    fontWeight: '700',
  },
});

export default App;
